public class SecondSequence {
    public static void main(String[]args) {
        
        int number = 2;
        for (int i = 0; i < 6; i++) { // A sequência parte do 2 e segue em multiplicação por 2, bastando multiplicar e usar o for para a repetição.
            System.out.print(number + " ");
            number *= 2;
        }
    }
}